package com.akshay;

public class Floor {

    private String floorColor;
    private String style;

    public Floor(String floorColor, String style) {
        this.floorColor = floorColor;
        this.style = style;
    }

    public String getFloorColor() {
        return floorColor;
    }

    public String getStyle() {
        return style;
    }
}
